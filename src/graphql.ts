export interface GenerateSchemaFromGraphQlOptions {
  importFrom?: string;
  schemaExportName?: string;
  header?: string;
}

interface IntrospectionDocument {
  data?: {
    __schema?: IntrospectionSchema;
  };
  __schema?: IntrospectionSchema;
}

interface IntrospectionSchema {
  queryType?: { name?: string | null } | null;
  types?: readonly IntrospectionType[];
}

interface IntrospectionType {
  kind: string;
  name?: string | null;
  fields?: readonly IntrospectionField[] | null;
  enumValues?: readonly { name?: string | null }[] | null;
  possibleTypes?: readonly { name?: string | null }[] | null;
}

interface IntrospectionField {
  name: string;
  type: IntrospectionTypeRef;
}

interface IntrospectionTypeRef {
  kind: string;
  name?: string | null;
  ofType?: IntrospectionTypeRef | null;
}

interface NamedTypeRef {
  name: string;
  kind: string;
  isList: boolean;
  nullable: boolean;
}

interface ResourceDescriptor {
  key: string;
  path: string;
  kind: 'collection' | 'single';
  entityName: string;
  type: IntrospectionType;
}

const DEFAULT_IMPORT_FROM = 'strapi-query';
const INTERNAL_TYPE_PREFIXES = [
  '__',
  'Comments',
  'UsersPermissions',
  'ReviewWorkflows',
  'I18N',
  'Error',
  'Pagination',
  'ResponseCollectionMeta'
];

export function generateSchemaFromGraphQl(
  introspection: unknown,
  options: GenerateSchemaFromGraphQlOptions = {}
) {
  const schema = getIntrospectionSchema(introspection);
  const types = new Map(
    (schema.types ?? [])
      .filter((type): type is IntrospectionType & { name: string } => Boolean(type.name))
      .map((type) => [type.name, type])
  );
  const resources = discoverResources(schema, types);
  const resourceByTypeName = new Map(resources.map((resource) => [resource.entityName, resource]));
  const extraTypes = discoverExtraObjectTypes(resources, types, resourceByTypeName);
  const generatedTypeNames = new Set([
    ...resources.map((resource) => resource.entityName),
    ...extraTypes.map((type) => type.name)
  ]);
  const schemaExportName = options.schemaExportName ?? 'schema';
  const importFrom = options.importFrom ?? DEFAULT_IMPORT_FROM;
  const header = options.header ?? 'Generated by strapi-query from Strapi GraphQL introspection.';
  const lines: string[] = [
    `// ${header}`,
    `import { collection, defineSchema, entity, many, one, single } from '${importFrom}';`,
    ''
  ];

  for (const resource of resources) {
    lines.push(renderInterface(resource.entityName, resource.type, types, resourceByTypeName, generatedTypeNames, true), '');
  }

  for (const type of extraTypes) {
    lines.push(renderInterface(type.name, type, types, resourceByTypeName, generatedTypeNames, false), '');
  }

  lines.push(`export const ${schemaExportName} = defineSchema({`);

  resources.forEach((resource, index) => {
    const relations = discoverRelations(resource, resourceByTypeName);
    lines.push(...indentLines(renderResource(resource, relations), 2));

    if (index < resources.length - 1) {
      lines[lines.length - 1] += ',';
    }
  });

  lines.push('});', '');
  return lines.join('\n');
}

